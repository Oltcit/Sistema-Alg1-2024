package modelo;

public class AlumnoVO {
	
	private int dni;
	private String ape;
	private String fecha;
	private byte doc;
	public int getDni() {
		return dni;
	}
	public void setDni(int dni) {
		this.dni = dni;
	}
	public String getApe() {
		return ape;
	}
	public void setApe(String ape) {
		this.ape = ape;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	public byte getDoc() {
		return doc;
	}
	public void setDoc(byte doc) {
		this.doc = doc;
	}
	
	
	
}
