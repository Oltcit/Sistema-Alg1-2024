package modelo;

public class MateriaVO {

	private String cod;
	private String nom;
	private int modulos;
	public String getCod() {
		return cod;
	}
	public void setCod(String cod) {
		this.cod = cod;
	}
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public int getModulos() {
		return modulos;
	}
	public void setModulos(int modulos) {
		this.modulos = modulos;
	}
	
	
	
}
